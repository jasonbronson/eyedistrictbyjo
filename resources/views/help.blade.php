                                <h3 class="cms-accordion__title block-title">What is Pupillary Distance (PD) and how can I measure it if it is not provided with my prescription?<span class="toggle"></span></h3>
                                <div class="cms-accordion__content content">
                                    <div class="cms-accordion__content-inner">
                                        <p>Your PD is the distance between your two pupils, expressed in millimeters. Sometimes the doctor will write two values (one for your left eye and one for your right, measured from the center of your nose) for your PD instead of one. Don't worry! These values will serve the same function as one value would, and there will be a space for you to enter this information when ordering.</p>

                                        <p>We need this measurement because it helps us to position the focus of your lens prescription in the right place for your frames. If your doctor did not include a PD measurement in your prescription, please feel free to use our <a href="/media/wysiwyg/pdfs/Eyedistrictbyjo-PD-measurement-guide-v1.pdf">simple guide (PDF)</a> to take a measurement for yourself. You can also head to the nearest Eyedistrictbyjo, where our trained opticians will gladly take a measurement for you.</p>
